// Some useful functions for the navigator

// The function to get the navigator app code name
export const navigatorAppCodeName = () => navigator.appCodeName;

// The function to get the navigator app name
export const navigatorAppName = () => navigator.appName;

// The function to get the navigator app version
export const navigatorAppVersion = () => navigator.appVersion;

// The function to get the navigator cookie enabled
export const navigatorCookieEnabled = () => navigator.cookieEnabled;

// The function to get the navigator platform
export const navigatorPlatform = () => navigator.platform;

// The function to get the navigator product
export const navigatorProduct = () => navigator.product;

// The function to get the navigator user agent
export const navigatorUserAgent = () => navigator.userAgent;

// The function to get the navigator language
export const navigatorLanguage = () => navigator.language;

// The function to get the navigator languages
export const navigatorLanguages = () => navigator.languages;
